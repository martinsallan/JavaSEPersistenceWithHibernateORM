    private void btn_RestaurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RestaurarActionPerformed
        int[] selectedRows = jTable2.getSelectedRows();
        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "Selecione um ou mais materiais para restaurar.");
            return;
        }

        int resposta = JOptionPane.showConfirmDialog(this, "Deseja restaurar " + selectedRows.length + " material(s)?", "Confirmação", JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            for (int viewRow : selectedRows) {
                int modelRow = jTable2.convertRowIndexToModel(viewRow);
                Long id = (Long) lixeiraTableModel.getValueAt(modelRow, 0);
                materialRepository.restore(id); // Restaura o item (muda ativo para true)
            }
            carregarTabelas();
        }
    }//GEN-LAST:event_btn_RestaurarActionPerformed

    private void btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarActionPerformed
        String nomeBusca = txt_Nome.getText().trim();
        if (!nomeBusca.isEmpty()) {
            // Se houver texto, busca pelo nome
            List<Material> resultados = materialRepository.findByNome(nomeBusca);
            ativasTableModel.setMateriais(resultados);
        } else {
            // Se o campo de busca estiver vazio, recarrega todos os materiais ativos
            carregarTabelas();
        }
    }//GEN-LAST:event_btn_BuscarActionPerformed

    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
        try {
            // Se nenhum material estiver selecionado, cria um novo. Senão, usa o selecionado para atualizar.
            Material material = (materialSelecionado == null) ? new Material() : materialSelecionado;

            // Pega os dados dos campos de texto e atribui ao objeto
            material.setNome(txt_Nome.getText());
            material.setTipo(txt_Tipo.getText());
            material.setQuantidadeEmEstoque(Double.parseDouble(txt_Quantidade.getText()));
            material.setUnidade(txt_Unidade.getText());
            material.setNivelMinimo(Double.parseDouble(txt_NivelMinimo.getText()));
            material.setAtivo(chk_Ativo.isSelected());

            // Salva ou atualiza no banco de dados
            materialRepository.saveOrUpdate(material);

            JOptionPane.showMessageDialog(this, "Material salvo com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            // Limpa o formulário e recarrega as tabelas para exibir a informação atualizada
            limparFormulario();
            carregarTabelas();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Os campos 'Estoque' e 'Nível Mínimo' devem ser números válidos.", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Erro de Validação: " + e.getMessage(), "Dados Inválidos", JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao salvar o material: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_SalvarActionPerformed

    private void btn_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AlterarActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione um material na tabela para alterar.");
            return;
        }

        // A seleção na tabela já chama o preencherFormulario, mas este botão
        // garante que o item correto seja carregado para edição.
        int modelRow = jTable1.convertRowIndexToModel(selectedRow);
        materialSelecionado = ativasTableModel.getMaterialEm(modelRow);
        preencherFormulario(materialSelecionado);
    }//GEN-LAST:event_btn_AlterarActionPerformed

    private void btn_DeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeletarActionPerformed
        int[] selectedRows = jTable1.getSelectedRows();
        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "Selecione um ou mais materiais para mover para a lixeira.");
            return;
        }

        int resposta = JOptionPane.showConfirmDialog(this, "Deseja mover " + selectedRows.length + " material(s) para a lixeira?", "Confirmação", JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            for (int viewRow : selectedRows) {
                int modelRow = jTable1.convertRowIndexToModel(viewRow);
                // Pega o ID da coluna 0 (a primeira coluna) do item selecionado
                Long id = (Long) ativasTableModel.getValueAt(modelRow, 0);
                materialRepository.softDelete(id); // Move para a lixeira (muda ativo para false)
            }
            carregarTabelas();
            limparFormulario();
        }
    }//GEN-LAST:event_btn_DeletarActionPerformed

    private void btn_DeletarPermanenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeletarPermanenteActionPerformed
        int[] selectedRows = jTable2.getSelectedRows();
        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "Selecione um ou mais itens para excluir permanentemente.");
            return;
        }

        int resposta = JOptionPane.showConfirmDialog(this, 
            "Excluir permanentemente " + selectedRows.length + " material(s)?\n\nESSA AÇÃO NÃO PODE SER DESFEITA.", 
            "Atenção!", 
            JOptionPane.YES_NO_OPTION, 
            JOptionPane.WARNING_MESSAGE);

        if (resposta == JOptionPane.YES_OPTION) {
            for (int viewRow : selectedRows) {
                int modelRow = jTable2.convertRowIndexToModel(viewRow);
                Long id = (Long) lixeiraTableModel.getValueAt(modelRow, 0);
                materialRepository.hardDelete(id); // Deleta do banco de dados para sempre
            }
            carregarTabelas();
        }
    }//GEN-LAST:event_btn_DeletarPermanenteActionPerformed

