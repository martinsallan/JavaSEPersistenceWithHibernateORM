    private void inicializarComponentesLogicos() {
        this.materialRepository = new MaterialRepository();
        this.ativasTableModel = new MaterialTableModel();
        this.jTable1.setModel(ativasTableModel); // Usa jTable1
        this.lixeiraTableModel = new MaterialTableModel();
        this.jTable2.setModel(lixeiraTableModel); // Usa jTable2
        carregarTabelas();
        adicionarListenerSelecaoTabela();
    }

    private void carregarTabelas() {
        List<Material> ativos = materialRepository.findAllActive();
        ativasTableModel.setMateriais(ativos);
        List<Material> excluidos = materialRepository.findAllInTrash();
        lixeiraTableModel.setMateriais(excluidos);
    }

    private void adicionarListenerSelecaoTabela() {
        jTable1.getSelectionModel().addListSelectionListener(event -> {
            if (!event.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
                int modelRow = jTable1.convertRowIndexToModel(jTable1.getSelectedRow());
                materialSelecionado = ativasTableModel.getMaterialEm(modelRow);
                preencherFormulario(materialSelecionado);
            }
        });
    }

    private void preencherFormulario(Material material) {
        if (material != null) {
            txt_Nome.setText(material.getNome());
            txt_Tipo.setText(material.getTipo());
            txt_Quantidade.setText(String.valueOf(material.getQuantidadeEmEstoque()));
            txt_Unidade.setText(material.getUnidade());
            txt_NivelMinimo.setText(String.valueOf(material.getNivelMinimo()));
            chk_Ativo.setSelected(material.getAtivo() != null && material.getAtivo());
        }
    }

    private void limparFormulario() {
        materialSelecionado = null;
        txt_Nome.setText("");
        txt_Tipo.setText("");
        txt_Quantidade.setText("");
        txt_Unidade.setText("");
        txt_NivelMinimo.setText("");
        chk_Ativo.setSelected(false);
        jTable1.clearSelection();
    }
    
