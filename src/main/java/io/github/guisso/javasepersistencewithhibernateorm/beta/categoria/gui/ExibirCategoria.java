    private void inicializarComponentesLogicos() {
        this.categoriaRepository = new CategoriaRepository();
        this.ativasTableModel = new CategoriaTableModel();
        this.jTable1.setModel(ativasTableModel);
        this.lixeiraTableModel = new CategoriaTableModel();
        this.jTable2.setModel(lixeiraTableModel);
        carregarTabelas();
        adicionarListenerSelecaoTabela();
    }

    private void carregarTabelas() {
        List<Categoria> ativas = categoriaRepository.findAllActive();
        ativasTableModel.setCategorias(ativas);
        List<Categoria> excluidas = categoriaRepository.findAllInTrash();
        lixeiraTableModel.setCategorias(excluidas);
    }

    private void adicionarListenerSelecaoTabela() {
        jTable1.getSelectionModel().addListSelectionListener(event -> {
            if (!event.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
                int modelRow = jTable1.convertRowIndexToModel(jTable1.getSelectedRow());
                categoriaSelecionada = ativasTableModel.getCategoriaEm(modelRow);
                preencherFormulario(categoriaSelecionada);
            }
        });
    }

    private void preencherFormulario(Categoria categoria) {
        if (categoria != null) {
            txt_Nome.setText(categoria.getNome());
            txt_Descricao.setText(categoria.getDescricao());
            chk_Ativo.setSelected(categoria.getAtivo() != null && categoria.getAtivo());
        }
    }

    private void limparFormulario() {
        categoriaSelecionada = null;
        txt_Nome.setText("");
        txt_Descricao.setText("");
        chk_Ativo.setSelected(true);
        jTable1.clearSelection();
    }
    
