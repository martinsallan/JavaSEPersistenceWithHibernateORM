    private void btn_RestaurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RestaurarActionPerformed
        int[] selectedRows = jTable2.getSelectedRows();
        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "Selecione uma ou mais categorias para restaurar.");
            return;
        }
        int resposta = JOptionPane.showConfirmDialog(this, "Restaurar " + selectedRows.length + " categoria(s)?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            for (int row : selectedRows) {
                int modelRow = jTable2.convertRowIndexToModel(row);
                Long id = (Long) lixeiraTableModel.getValueAt(modelRow, 0);
                categoriaRepository.restore(id);
            }
            carregarTabelas();
        }
    }//GEN-LAST:event_btn_RestaurarActionPerformed

    private void btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarActionPerformed
        String nomeBusca = txt_Nome.getText().trim();
        if (!nomeBusca.isEmpty()) {
            List<Categoria> resultados = categoriaRepository.findByNome(nomeBusca);
            ativasTableModel.setCategorias(resultados);
        } else {
            carregarTabelas();
        }
    }//GEN-LAST:event_btn_BuscarActionPerformed

    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
        try {
            Categoria categoria = (categoriaSelecionada == null) ? new Categoria() : categoriaSelecionada;
            categoria.setNome(txt_Nome.getText());
            categoria.setDescricao(txt_Descricao.getText());
            categoria.setAtivo(chk_Ativo.isSelected());
            categoriaRepository.saveOrUpdate(categoria); // Ou saveOrUpdate, se for o caso
            JOptionPane.showMessageDialog(this, "Categoria salva com sucesso!");
            limparFormulario();
            carregarTabelas();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar categoria: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_SalvarActionPerformed

    private void btn_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AlterarActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma categoria para alterar.");
            return;
        }
        int modelRow = jTable1.convertRowIndexToModel(selectedRow);
        categoriaSelecionada = ativasTableModel.getCategoriaEm(modelRow);
        preencherFormulario(categoriaSelecionada);
    }//GEN-LAST:event_btn_AlterarActionPerformed

    private void btn_DeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeletarActionPerformed
        int[] selectedRows = jTable1.getSelectedRows();
        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "Selecione uma ou mais categorias para mover para a lixeira.");
            return;
        }
        int resposta = JOptionPane.showConfirmDialog(this, "Mover " + selectedRows.length + " categoria(s) para a lixeira?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            for (int row : selectedRows) {
                int modelRow = jTable1.convertRowIndexToModel(row);
                Long id = (Long) ativasTableModel.getValueAt(modelRow, 0);
                categoriaRepository.softDelete(id);
            }
            carregarTabelas();
            limparFormulario();
        }
    }//GEN-LAST:event_btn_DeletarActionPerformed

    private void btn_DeletarPermanenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeletarPermanenteActionPerformed
        int[] selectedRows = jTable2.getSelectedRows();
        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "Selecione um ou mais itens para excluir permanentemente.");
            return;
        }
        int resposta = JOptionPane.showConfirmDialog(this, "Excluir permanentemente " + selectedRows.length + " categoria(s)?\nESSA AÇÃO NÃO PODE SER DESFEITA.", "Atenção!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (resposta == JOptionPane.YES_OPTION) {
            for (int row : selectedRows) {
                int modelRow = jTable2.convertRowIndexToModel(row);
                Long id = (Long) lixeiraTableModel.getValueAt(modelRow, 0);
                categoriaRepository.hardDelete(id);
            }
            carregarTabelas();
        }
    }//GEN-LAST:event_btn_DeletarPermanenteActionPerformed

